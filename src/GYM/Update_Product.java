    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          int pid = Integer.parseInt(Pid.getText());

       String Pn = Pname.getText();
       String Pb = Pbrand.getText();

        int sq = Integer.parseInt(Sqa.getText());
        int bp = Integer.parseInt(buy.getText());
        int se = Integer.parseInt(sell.getText());
        String des = desc.getText();
        
        DataBase_Connect dc = new DataBase_Connect();

        try {
            String da = "UPDATE product SET Product_Name='"+Pn+"',Product_Brand='"+Pb+"',Quantity='"+sq+"',Buy='"+bp+"',Sell='"+se+"',Description='"+des+"' WHERE P_ID = '"+pid+"'";
            dc.St.executeUpdate(da);
            clear();
            JOptionPane.showMessageDialog(null, "Successfully Updated Product Details");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Can't Update Details....!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

