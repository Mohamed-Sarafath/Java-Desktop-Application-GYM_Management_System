    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int pid = Integer.parseInt(Pid.getText());

        DataBase_Connect dc = new DataBase_Connect ();

        try
        {
            String da = "SELECT * FROM product WHERE P_ID = '"+pid+"'";
            ResultSet rs = dc.St.executeQuery(da);

            while(rs.next())
            {

                
                Pname.setText(rs.getString("Product_Name"));
                Pbrand.setText(rs.getString("Product_Brand"));
                Sqa.setText(rs.getString("Quantity"));
                buy.setText(rs.getString("Buy"));
                sell.setText(rs.getString("Sell"));
                desc.setText(rs.getString("Description"));

            }
        }

        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

